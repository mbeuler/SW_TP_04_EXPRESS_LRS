int format_channels(char *msg, int n)
{
	int pos = 0;

	for (int i = 0; i < n; i++) {
		uint16_t v = rc_us[i];
		// Tausenderstelle
		if (v >= 1000) msg[pos++] = '0' + (v / 1000);
		else           msg[pos++] = ' ';
		// Hunderterstelle
		if (v >= 100)  msg[pos++] = '0' + ((v / 100) % 10);
		else           msg[pos++] = ' ';
		// Zehnerstelle
		if (v >= 10)   msg[pos++] = '0' + ((v / 10) % 10);
		else           msg[pos++] = ' ';
		// Einerstelle
		msg[pos++] = '0' + (v % 10);

		// Trennzeichen (au√üer nach letztem Kanal)
		if (i < n - 1) msg[pos++] = ' ';
	}

	// Zeilenende
	msg[pos++] = '\r';
	msg[pos++] = '\n';
	msg[pos]   = '\0'; // String-Ende

	return pos;
}

